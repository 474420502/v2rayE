func normalizeUILanguage(lang string) string {
	normalized := strings.ToLower(strings.TrimSpace(lang))
	switch {
	case strings.HasPrefix(normalized, "zh"):
		return uiLangZH
	case strings.HasPrefix(normalized, "en"):
		return uiLangEN
	default:
		return uiLangEN
	}
}

func detectDefaultUILanguage() string {
	for _, key := range []string{"V2RAYE_UI_LANG", "LANG", "LC_ALL"} {
		if value := strings.TrimSpace(os.Getenv(key)); value != "" {
			return normalizeUILanguage(value)
		}
	}
	return uiLangEN
}

func setGlobalUILanguage(lang string) {
	globalUILanguage.Store(normalizeUILanguage(lang))
}

func currentGlobalUILanguage() string {
	if value, ok := globalUILanguage.Load().(string); ok {
		return normalizeUILanguage(value)
	}
	return uiLangEN
}

func tr(lang, key string) string {
	if group, ok := uiTextCatalog[normalizeUILanguage(lang)]; ok {
		if value, ok := group[key]; ok {
			return value
		}
	}
	if fallback, ok := uiTextCatalog[uiLangEN][key]; ok {
		return fallback
	}
	return key
}

func (a *tuiApp) t(key string) string {
	return tr(currentGlobalUILanguage(), key)
}

func (a *tuiApp) tf(key string, args ...any) string {
	return fmt.Sprintf(a.t(key), args...)
}

func (a *tuiApp) setUILanguage(lang string) {
	normalized := normalizeUILanguage(lang)
	a.mu.Lock()
	a.uiLanguage = normalized
	if a.config != nil {
		a.config["uiLanguage"] = normalized
	}
	a.mu.Unlock()
	setGlobalUILanguage(normalized)

	a.runUI(func(app *tview.Application) {
		a.syncPages()
		a.footerStatus = a.t("status.language.switched")
		a.refreshFooter()
	})
}

func configuredUILanguageFromConfig(config map[string]any) (string, bool) {
	for _, key := range []string{"uiLanguage", "language", "lang"} {
		if value := strings.TrimSpace(stringValue(config, key)); value != "" {
			return normalizeUILanguage(value), true
		}
	}
	return "", false
}

func (a *tuiApp) applyConfiguredUILanguage(config map[string]any) {
	lang, ok := configuredUILanguageFromConfig(config)
	if !ok {
		return
	}

	a.mu.Lock()
	current := normalizeUILanguage(a.uiLanguage)
	a.uiLanguage = lang
	if a.config != nil {
		a.config["uiLanguage"] = lang
	}
	a.mu.Unlock()
	setGlobalUILanguage(lang)

	if current == lang {
		return
	}

	a.runUI(func(app *tview.Application) {
		a.syncPages()
		a.refreshFooter()
	})
}

func (a *tuiApp) toggleUILanguageAction(context.Context) error {
	a.mu.Lock()
	lang := a.uiLanguage
	a.mu.Unlock()
	if normalizeUILanguage(lang) == uiLangZH {
		a.setUILanguage(uiLangEN)
	} else {
		a.setUILanguage(uiLangZH)
	}
	return nil
}

func (a *tuiApp) selectUILanguageEnglishAction(context.Context) error {
	a.setUILanguage(uiLangEN)
	a.setFooter(a.t("settings.lang.footer.en"))
	return nil
}

func (a *tuiApp) selectUILanguageChineseAction(context.Context) error {
	a.setUILanguage(uiLangZH)
	a.setFooter(a.t("settings.lang.footer.zh"))
	return nil
}
